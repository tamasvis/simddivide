/*-------------------------------------
 * returns >0 if matched at least one parameter; number of arguments consumed
 *         0  if no parameters are removed, prm/args are missing
 *            (callers are expected to filter the latter)
 *         <0 if matching failed: -1-based index of first rejected argument
 *            option referencing beyond vcount would fail this way
 *
 *            checks only matched parameters; unmatched table entries are
 *            not verified
 *
 * updates val[] entries, adding/removing bit/masks as appropriate
 * sanity-checks params with (NULL,0) (val,vcount), not checking vcount
 *                      ...against vcount too, with (NULL,>0) (val,vcount)
 *
 * expects non-NULL prms and arguments, pcount >0, returning 0 otherwise
 */
static INLINE
long prm_getopts(uint64_t *val,  unsigned int vcount,
     const struct CU_Opts *prms, unsigned int pcount,
                      int argc,    const char **argv)
{
	int done = 0;
	long n = 0;

	if ((NULL == prms) || (0 == pcount) || (0 == argc) || (NULL == argv))
		return 0;

	while ((0 < argc--) && !done) {
		unsigned int pi;

		for (pi = 0; pi<pcount; ++pi) {
			unsigned int vi = prms[ pi ].validx;
			size_t arglen = 0;
			int matched = 0;

			if ((NULL == prms[ pi ].prmstr) ||
			    (0 == prms[ pi ].pbytes))
				continue;

			if (CU__LEAD_CHAR == argv[0][0])
				arglen = strlen(argv[0]);
			if (!arglen) {
				done = 1;    /* first non-"--<...>" argument */
				break;
			}

			if ((0 != prms[ pi ].add)                           &&
			    (CU__ADD_PREF_BYTES +prms[pi].pbytes == arglen) &&
			    !memcmp(argv[0], CU__ADD_PREF,
			            CU__ADD_PREF_BYTES)                     &&
			    !memcmp(&( argv[0][ CU__RM_PREF_BYTES ] ),
			            prms[ pi ].prmstr, prms[ pi ].pbytes))
			{
				++matched;
				if ((NULL != val) && (vi < vcount))
					val[vi] |= prms[ pi ].add;
			}

			if ((0 != prms[ pi ].remove)                       &&
			    (CU__RM_PREF_BYTES +prms[pi].pbytes == arglen) &&
			    !memcmp(argv[0], CU__RM_PREF,
			            CU__RM_PREF_BYTES)                     &&
			    !memcmp(&( argv[0][ CU__RM_PREF_BYTES ] ),
			            prms[ pi ].prmstr, prms[ pi ].pbytes))
			{
				++matched;
				if ((NULL != val) && (vi < vcount))
					val[vi] &= ~( prms[ pi ].remove );
			}

			if (matched && (0 != vcount) && (vcount < vi))
				matched = 0;    /* ref'd outside val[vcount] */

			if (!matched) {                    /* invalid option */
				n = -1-n;
				break;
			}
		}
		if ((n < 0) || done)
			break;

		++argv;
		++n;
	}

	return n;
}

