/*--------------------------------------
 * returns <0 if any read failed
 *         nr. of bytes read, possibly 0, upon success
 *
 *     NULL *start gets newly allocated; returned to caller if successful
 * non-NULL *start gets used as (*start, maxbytes)
 */
static INLINE
long readall_fd(unsigned char **start, size_t maxbytes, FILE *rdfd)
{
	unsigned int alloc_out;
	size_t buflen = 0, rd;
	long rc = 0, all = 0;

	if (!rdfd || !start)
		return -1;

	alloc_out = (*start == NULL);

	do {
	unsigned char *next;

	buflen += CU__FILECHUNK_BYTES;

	if (alloc_out) {
		next = (unsigned char *) realloc(*start, buflen);
		if (NULL == next) {
			fprintf(stderr, "fileread/no memory[%zu b]\n", buflen);
			rc = -2;
			break;
		}
		*start = next;
	}

	rd = fread(*start +buflen -CU__FILECHUNK_BYTES, 1,
	           CU__FILECHUNK_BYTES, rdfd);

// TODO: SNH check for wrap/overflow?

	if (maxbytes && (maxbytes < (all +rd))) {
		fprintf(stderr, "file bytecount out of range[%zu b]\n", buflen);
		rc = -4;
		break;
	}

	all += rd;
	} while ((0 < rd) && !feof(rdfd) && !ferror(rdfd));

	if (rc < 0) {
		fflush(stderr);

		if (alloc_out && *start) {
			free(*start);
			*start = NULL;
		}
	}

	return (rc < 0) ? rc : all;
}


